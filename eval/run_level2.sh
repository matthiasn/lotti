latest_run_id() {
  if [[ ! -d "$RUNS_ROOT" ]]; then
    echo "!!! No eval run directory exists under ${RUNS_ROOT}." >&2
    exit 6
  fi
  local latest
  latest="$(find "$RUNS_ROOT" -mindepth 1 -maxdepth 1 -type d -print 2>/dev/null | sort | tail -n 1 || true)"
  if [[ -z "$latest" ]]; then
    echo "!!! No eval runs found under ${RUNS_ROOT}." >&2
    exit 6
  fi
  basename "$latest"
}

