import 'dart:typed_data';

import 'package:flutter/painting.dart';

/// A 1x1 teal-pixel PNG for use in design system tests that need an
/// [ImageProvider] without touching the file system or network.
final dsPlaceholderImage = MemoryImage(dsPlaceholderPngBytes);

/// Raw bytes of a minimal valid 1x1 PNG image.
final Uint8List dsPlaceholderPngBytes = Uint8List.fromList(const [
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // 8-bit RGB
  0xde, 0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, // IDAT chunk
  0x54, 0x08, 0xd7, 0x63, 0x90, 0x98, 0xd8, 0x00, // compressed
  0x00, 0x00, 0x04, 0x00, 0x01, 0xf6, 0x17, 0x8e,
  0xaa, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, // IEND chunk
  0x44, 0xae, 0x42, 0x60, 0x82,
]);
