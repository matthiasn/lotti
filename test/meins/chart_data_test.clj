(ns meins.chart-data-test
  (:require [clojure.test :refer [deftest testing is]]
            [meins.electron.renderer.charts.data :as cd]))

(deftest remaining-times-test
  (testing "handles empty maps"
    (is (= {}
           (cd/remaining-times {} {}))))
  (testing "handles nil values"
    (is (= {}
           (cd/remaining-times nil nil))))
  (testing "handles nil for actual times"
    (is (= {1486080814718 18000
            1486206401941 10800}
           (cd/remaining-times nil {1486206401941 10800
                                    1486080814718 18000}))))
  (testing "remaining times calculated properly"
    (is (= {1485898032295 2700
            1486080338498 3169
            1486080814718 18000
            1486080841220 1516
            1486080959114 1200
            1486206401941 10800}
           (cd/remaining-times
             {1486080338498 2231
              1486080841220 1184
              1486157861454 1863
              1486157703892 842
              1486080959114 2400}
             {1486206401941 10800
              1486080814718 18000
              1486080338498 5400
              1486080841220 2700
              1485898032295 2700
              1486080959114 3600
              1486157861454 1800})))))

